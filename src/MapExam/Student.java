package MapExam;

public class Student {
	private int snum;
	private String name;
	Student(int snum, String name) {
		super();
		this.snum = snum;
		this.name = name;
	}
	public int getSnum() {
		return snum;
	}
	public void setSnum(int snum) {
		this.snum = snum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
